<?php
////前置运算符 和后置运算符  区别
$a=0;
echo '<br>$a++-----------<br>';
for($b=0 ; $b<=10 ;$b++)
{
	echo $a++;
}
$a=0;
echo '<br>++$a------------<br>';
for($b=0 ; $b<=10 ; $b++)
{
	echo ++$a;
}
$c=10;
echo '<br>$c-- ------------<br>';
for($b=10 ;$b>=0 ; $b--)
{
	echo $c--;
}
$c=10;
echo '<br>--$c -------------<br>';
for($b=10 ; $b>=0 ; $b--)
{
	echo --$c;
}